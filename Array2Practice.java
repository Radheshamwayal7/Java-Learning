public class Array2Practice {
    public static void main(String[] args) {
        
        int marks[] = new int[3];

        marks[0] = 22;
        marks[1] = 55;
        marks[2] = 77;

        for(int i = 0; i < marks.length; i++) {
            System.out.println("Marks="+marks[i]);
        }
    }
}
